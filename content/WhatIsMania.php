			Was ist die VMP Mania?
		</span>
		<div class="maniaDescription">
		Die Idee zur VMP Mania kam anfang 2011 von Kakadu, der vorschlug, einmal im Jahr ein Claninternes Turnier abzuhalten, bei dem jedes Mitglied des VMP Clans im 1 vs. 1 
		gegeneinander antritt. Damit sollte der unangefochtene MVP (Most Valuable Player) eines jeden Clanspiels ausgespielt werden. Welche Disziplinen, also in unserem Fall
		Spiele zur Mania angeboten werden, hängt dabei in erster Linie immer von unseren Mitgliedern ab. Neben den Dauerbrennern wie Call of Duty kann jedes Teilnahmeberechtigte
		Mitglied weitere Spiele vorschlagen. Finden sich dafür genug Interessenten, wird auch hier ein Sieger ausgespielt. Das Event findet seit 2011 jedes Jahr statt, früher im 
		Februar, mittlerweile im Herbst.
